/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    vector<double> averageOfLevels(TreeNode* root) {
        if(!root) return {};
        vector<double> r;
        queue<TreeNode*> q;
        q.push(root);
        while(q.size()) {
            int n = q.size();
            double f = 0;
            for(int i=0;i<n;i++) {
                TreeNode *x = q.front();q.pop();
                if(x->left) q.push(x->left);
                if(x->right) q.push(x->right);
                f+=x->val;
            }
            r.push_back(f/n);
        }
        return r;
    }
};
// @lc code=end


int main() {
    cout << " * [637] 二叉树的层平均值" << endl;
    Solution k;
    vector<int> f = {3,9,20,15,7};
    auto node = makeTree(f);
    printt("-",node,false);
    auto w = k.averageOfLevels(node);
    for (auto u:w) print(u);
    return 0;
}
