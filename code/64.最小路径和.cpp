// @lc code=start
class Solution {
public:
    int minPathSum(vector<vector<int>>& grid) {
        int n = grid.size();
        if(!n) return  0;
        int m = grid[0].size();
        if(!m) return 0;
        vector<vector<int> > dp(n,vector<int> (m,500));
        dp[0][0] = grid[0][0];
        for(int i=1;i<n;i++) dp[i][0] = grid[i][0] + dp[i-1][0];
        for(int i=1;i<m;i++) dp[0][i] = grid[0][i] + dp[0][i-1];
        for (int i=1;i<n;i++) {
            for (int j=1;j<m;j++) {
                dp[i][j] = min(dp[i-1][j],dp[i][j-1]) + grid[i][j];
            }
        }
        return dp[n-1][m-1];

    }
};
// @lc code=end

int main() {
    cout << " * [64] 最小路径和" << endl;
    Solution k;
    
    vector<vector<int> > re = {{1,3,1},{1,5,1},{4,2,1}};
    cout << k.minPathSum(re);
    return 0;
}
