/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* middleNode(ListNode* head) {
        auto p = head;
        auto q = head;
        while(q && q->next) {
            q = goNext(q);
            p = p->next;
            
        }
        return p;

    }
    ListNode *goNext(ListNode *p) {
        if(p->next) {
            p = p->next;
        }
        if(p->next) {
            p = p->next;
        }
        return p;
    }
};
// @lc code=end

