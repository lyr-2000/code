// @lc code=start
class Solution {
public:
    string maximumOddBinaryNumber(string s) {
        int n = s.size();
        string buf(n,'0');
        int c=0;
        for (char f:s) if(f=='1') c++;
        if(c) {
            buf[n-1] = '1';
            c--;
        }
        for (int i=0;i<c;i++) buf[i] = '1';
        return buf;
    }
};
// @lc code=end

