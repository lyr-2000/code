// @lc code=start
/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    public int maxPathSum(TreeNode root) {
        return root.val+ maxCarry(root);
    }

    private int maxCarry(TreeNode root)
    {
        if(root==null) return 0;
        int l = maxCarry(root.left);
        int r = maxCarry(root.right);
        return l>=r? l+root.val:r+root.val;
    }

}
